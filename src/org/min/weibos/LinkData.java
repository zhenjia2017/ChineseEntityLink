package org.min.weibos;

public class LinkData {

	private String id;
	private String name;
	private int startoffset;
	private int entoffset;
	private String kb;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getStartoffset() {
		return startoffset;
	}
	public void setStartoffset(int startoffset) {
		this.startoffset = startoffset;
	}
	public int getEntoffset() {
		return entoffset;
	}
	public void setEntoffset(int entoffset) {
		this.entoffset = entoffset;
	}
	public String getKb() {
		return kb;
	}
	public void setKb(String kb) {
		this.kb = kb;
	}

}
